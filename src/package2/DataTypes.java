package package2;

public class DataTypes {
	

	public static void main(String[] args) {
		//Integer example
		int a = 5;
		int b = 7;
		System.out.println(a+b);
		
		//Double example
		double c = 2.5;
		double d = 2;
		System.out.println(c*d);
		
		//String example
		String name = "Darko";
		System.out.println(name);
		
		//Boolean example
		Boolean isGood = false;
		if(isGood) {
			System.out.println("Good!");
		}else {
			System.out.println("Bad");
		}
		
	}

}
